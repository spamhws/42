#include <stdio.h>

char	*ft_strupcase(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			str[i] = str[i] - 32;
		}
		i++;
	}
	return (str);
}

// Implement strcpy function in C
int		main(void)
{
	char source[] = "HASDH";

	// Copies the first num characters of source to destination
	printf("%s", ft_strupcase(source));

	return 0;
}
